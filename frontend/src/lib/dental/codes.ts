export interface ToothDef { code: string; label: string }

const PERM_POS = ['төв үүдэн', 'хажуу үүдэн', 'соёо', '1-р бага араа', '2-р бага араа', '1-р их араа', '2-р их араа', 'мэргэн шүд'];
const DECID_POS = ['төв үүдэн', 'хажуу үүдэн', 'соёо', '1-р араа', '2-р араа'];
const QUADRANT: Record<string, string> = {
  '1': 'баруун дээд', '2': 'зүүн дээд', '3': 'зүүн доод', '4': 'баруун доод',
  '5': 'баруун дээд', '6': 'зүүн дээд', '7': 'зүүн доод', '8': 'баруун доод',
};

// FDI two-digit notation: first digit = quadrant, second = position from midline.
export function toothLabel(code: string): string {
  const q = code[0];
  const p = parseInt(code[1], 10);
  const permanent = q <= '4';
  const pos = permanent ? PERM_POS[p - 1] : DECID_POS[p - 1];
  if (!pos || !QUADRANT[q]) return code;
  return `${QUADRANT[q]} ${permanent ? 'байнгын' : 'сүүн'} ${pos}`;
}

